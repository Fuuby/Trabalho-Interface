    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        
        ArrayList<Pessoa> listpess = new ArrayList<Pessoa>();
        listpess = (ArrayList<Pessoa>) arqin.getTodasPessoas();

       

        listpess.get(linha).setNome(txtNome.getText());
        listpess.get(linha).setRg(txtRG.getText());
        listpess.get(linha).setCpf(txtCPF.getText());
        listpess.get(linha).setDataNascimento(txtData.getText());
        listpess.get(linha).setCnh(txtCNH.getText());
        listpess.get(linha).setCep(txtCEP.getText());
        listpess.get(linha).setEstado((String) cbEstado.getSelectedItem());
        listpess.get(linha).setCidade(txtCidade.getText());
        listpess.get(linha).setBairro(txtBairro.getText());
        listpess.get(linha).setNumero(txtNumero.getText());
        listpess.get(linha).setEndereco(txtEndereco.getText());
        
        
        
        try {
            arqout.recadastrar_todos(listpess);
        } catch (IOException ex) {
            Logger.getLogger(CadastroPessoas.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null, "Registro alterado com sucesso!");

        limpar();

        DefaultTableModel modelo = (DefaultTableModel) jtPessoa.getModel();

        if (jtPessoa.getRowCount() > 0) {
            for (int i = jtPessoa.getRowCount() - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        }

        List<Pessoa> listauto2 = new ArrayList<Pessoa>();
        listauto2 = arqin.getTodasPessoas();

        for (int i = 0; i < listauto2.size(); i++) {
            modelo.addRow(listauto2.get(i).getpessoa());
        }
        
        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);
        
        
        
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jtPessoa.getModel();
        List<Pessoa> list = new ArrayList<Pessoa>();
        ArrayList<Pessoa> nova_list = new ArrayList<Pessoa>();
        linha = jtPessoa.getSelectedRow();
        Object valor = modelo.getValueAt(linha, 0);
        list = arqin.getTodasPessoas();

        for (int i = 0; i < list.size(); i++) {
            if (!(list.get(i).getNome().equals(valor))) {
                nova_list.add(list.get(i));
            }
        }
        try {
            arqout.recadastrar_todos(nova_list);
        } catch (IOException ex) {
            Logger.getLogger(CadastroPessoas.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelo.removeRow(linha);
        JOptionPane.showMessageDialog(null, "Registro apagado com sucesso");

        limpar();
        
        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);
    }//GEN-LAST:event_btExcluirActionPerformed

