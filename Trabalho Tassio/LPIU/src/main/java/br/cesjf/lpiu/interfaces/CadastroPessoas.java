    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        // TODO add your handling code here:
        pess = new Pessoa();
        pess.setNome(txtNome.getText());
        pess.setRg(txtRG.getText());
        pess.setCpf(txtCPF.getText());
        pess.setDataNascimento(txtData.getText());
        pess.setCnh(txtCNH.getText());
        pess.setCep(txtCEP.getText());
        pess.setEstado((String) cbEstado.getSelectedItem());
        pess.setCidade(txtCidade.getText());
        pess.setBairro(txtBairro.getText());
        pess.setNumero(txtNumero.getText());
        pess.setEndereco(txtEndereco.getText());

        try {
            arqout.Adicionar(pess);
        } catch (IOException ex) {
            Logger.getLogger(CadastroPessoas.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(
                null, "Pessoa inserida com sucesso!");

        limpar();

        int cont = 1;

        List<Pessoa> listapeople = new ArrayList<Pessoa>();

        listapeople = arqin.getTodasPessoas();
        DefaultTableModel modelopeople = (DefaultTableModel) jtPessoa.getModel();

        for (int i = jtPessoa.getRowCount() - 1;
                i >= 0; --i) {
            modelopeople.removeRow(i);
        }

        for (int i = 0;
                i < listapeople.size();
                i++) {
            modelopeople.addRow(listapeople.get(i).getpessoa());
            cont++;
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

