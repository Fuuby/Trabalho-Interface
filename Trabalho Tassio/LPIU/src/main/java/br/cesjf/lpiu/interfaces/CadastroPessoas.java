        for (int i = jtPessoa.getRowCount() - 1;
                i >= 0; --i) {
            modelopeople.removeRow(i);
        }

        for (int i = 0;
                i < listapess.size();
                i++) {
            modelopeople.addRow(listapess.get(i).getpessoa());
            cont++;
        }
        
